import numpy as np

class Sigmoid:
	def __init__(self):
		self.params = []

	def forward(self, x):
		return 1 / (1 + np.exp(-x))


class Affine:
	def __init__(self, W, b):
		self.params = [W, b]

	def forward(self, x):
		W, b = self.params
		out = np.dot(x, W) + b
		return out

class Softmax:
    def __init__(self):
        self.params, self.grads = [], []
        self.out = None

    def forward(self, x):
        self.out = softmax(x)
        return self.out

    def backward(self, dout):
        dx = self.out * dout
        sumdx = np.sum(dx, axis=1, keepdims=True)
        dx -= self.out * sumdx
        return dx


